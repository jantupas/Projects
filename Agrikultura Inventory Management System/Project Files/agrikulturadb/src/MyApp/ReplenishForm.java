    private void updateqtyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateqtyActionPerformed
        //replenishes product quantity and updates database
        if(idtxt.getText().isEmpty()||nametxt.getText().isEmpty()||qtytxt.getText().isEmpty())
            JOptionPane.showMessageDialog(null,"Please check that all inputs are filled!");
        else{
        DefaultTableModel model = (DefaultTableModel)ReplenishTable.getModel();

        int selectedRowIndex = ReplenishTable.getSelectedRow();
        prodid = Integer.parseInt((model.getValueAt(selectedRowIndex, 0).toString()));
        quantity = Integer.parseInt(qtytxt.getText()) + Integer.parseInt((model.getValueAt(selectedRowIndex, 4).toString()));
        
        mySql.updateQty(prodid, quantity);  //updates record quantity in database based on input
        showRepTable();
        resetAll();
        }
    }//GEN-LAST:event_updateqtyActionPerformed

    private void removeprodActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeprodActionPerformed
       //removes a product from database
        if(idtxt2.getText().isEmpty()||nametxt2.getText().isEmpty()||qtytxt2.getText().isEmpty())
            JOptionPane.showMessageDialog(null,"Please check that all inputs are filled!");
        else{
        DefaultTableModel model = (DefaultTableModel)ExpiredTable.getModel();

        int selectedRowIndex = ExpiredTable.getSelectedRow();
        prodid = Integer.parseInt((model.getValueAt(selectedRowIndex, 0).toString()));
        
        mySql.deleteExp(prodid);  //deletes a record of an expired product from database
        showExpTable();
        resetAll();
        }
    }//GEN-LAST:event_removeprodActionPerformed

