    public void clearReplenishTable()  //clears contents of replenish table
    {
        model1=(DefaultTableModel) ReplenishTable.getModel();
        model1.getDataVector().removeAllElements();
        model1.fireTableDataChanged();
    }
    
    public void clearExpiredTable() //clears contents of expired table
    {
        model2=(DefaultTableModel) ExpiredTable.getModel();
        model2.getDataVector().removeAllElements();
        model2.fireTableDataChanged();
    }
    
    public void showRepTable()      //shows data in rep table
    {
        clearReplenishTable();
        prods = mySql.displayRec();
        model1=(DefaultTableModel) ReplenishTable.getModel();
        for(Product p: prods)
           if(p.getQuantity() < 10 && "Good".equals(p.getStatus()))
               model1.addRow(new Object[] {p.getProdid(),p.getName(),p.getStatus(),df.format(p.getAcqprc()),p.getQuantity()});
    }
    
    public void showExpTable()      //shows data in exp table
    {       
        clearExpiredTable();
        prods = mySql.displayRec();
        model2=(DefaultTableModel) ExpiredTable.getModel();
        for(Product p: prods)
           if("Expired".equals(p.getStatus()))
               model2.addRow(new Object[] {p.getProdid(),p.getName(),p.getStatus(),p.getQuantity(),p.getExpdate()});
    }
    
