    private void processActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processActionPerformed
        //processes the products added to cart and to be sold
        if(idtxt.getText().isEmpty()||nametxt.getText().isEmpty()||qtytxt.getText().isEmpty()||prctxt.getText().isEmpty())
            JOptionPane.showMessageDialog(null,"Please check that all inputs are filled!");  //checks if all inputs are filled and appropriate
        else{
        model1=(DefaultTableModel) cartTable.getModel();
        model1.addRow(new Object[] {idtxt.getText(),nametxt.getText(),prctxt.getText(),qtytxt.getText()});
        prc = (Integer.parseInt(qtytxt.getText())) * (Double.parseDouble(prctxt.getText()));
        id = Integer.parseInt(idtxt.getText());
        name = nametxt.getText();
        price = Double.parseDouble(prctxt.getText());            
        qty = Integer.parseInt(qtytxt.getText());                   //gets data from input fields and updates quantity referring to how many products were sold
        total += prc;
        totaltxt.setText(Double.toString(total));
        mySql.createSellRec(id, name, qty, price);
        qty2 = Integer.parseInt(qty1) - qty;
        mySql.updateRec2(id, qty2);
        prc = 0;
        showAvailTable();
        resetAll();
        }
    }//GEN-LAST:event_processActionPerformed

    private void RevertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RevertActionPerformed
        //undos a product added to cart 
        if(idtxt.getText().isEmpty()||nametxt.getText().isEmpty()||qtytxt.getText().isEmpty()||prctxt.getText().isEmpty())
            JOptionPane.showMessageDialog(null,"Please check that all inputs are filled!");  //checks if all inputs are filled and appropriate
        else{
        id1 = Integer.parseInt(idtxt.getText());
        try {
            qty3 = mySql.reqQty(id1);    //gets quantity of selected product
        } catch (SQLException ex) {
            Logger.getLogger(SellForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        qty2 = Integer.parseInt(qtytxt.getText()) + qty3;

        mySql.deleteRec2(oldProdid);   //deletes record from database
        mySql.updateRec2(id1, qty2);   //updates record from database
        showAvailTable();
        prc = total - (Integer.parseInt(qtytxt.getText())) * (Double.parseDouble(prctxt.getText()));
        total = prc;
        totaltxt.setText(Double.toString(total));
        DefaultTableModel model = (DefaultTableModel)cartTable.getModel();

        selectedRowIndex1 = cartTable.getSelectedRow();
        model.removeRow(selectedRowIndex1);
        prc = 0;
        resetAll();
        }
    }//GEN-LAST:event_RevertActionPerformed

