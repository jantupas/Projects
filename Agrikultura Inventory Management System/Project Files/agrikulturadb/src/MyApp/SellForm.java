    public void clearavailTable()
    {
        model1=(DefaultTableModel) availTable.getModel();
        model1.getDataVector().removeAllElements();
        model1.fireTableDataChanged();
    }
    
    public void clearcartTable()
    {
        model2=(DefaultTableModel) cartTable.getModel();
        model2.getDataVector().removeAllElements();
        model2.fireTableDataChanged();
    }
  
    public void showAvailTable()
    {
        clearavailTable();
        prods = mySql.displayRec();
        model1=(DefaultTableModel) availTable.getModel();
        for(Product p: prods)
            if(p.getQuantity() > 0)
               model1.addRow(new Object[] {p.getProdid(),p.getName(),df.format(p.getSellprc()),p.getQuantity()});
    }
    
